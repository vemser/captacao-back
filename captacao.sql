CREATE TABLE CANDIDATO (
  ID_CANDIDATO  NUMBER,
  ID_FORMULARIO NUMBER NOT NULL,
  ID_EDICAO NUMBER NOT NULL,
  EMAIL VARCHAR2(255) NOT NULL,
  TELEFONE VARCHAR2(30) NOT NULL,
  RG VARCHAR2(30) NOT NULL,
  CPF VARCHAR2(30) NOT NULL,
  ESTADO VARCHAR2(50) NOT NULL,
  CIDADE VARCHAR2 (255) NOT NULL,
  PCD CHAR(1) NOT NULL,
  OBSERVACOES VARCHAR2(255),
  NOTA_COMPORTAMENTAL NUMBER,
  NOTA_TECNICA NUMBER,
  NOTA_PROVA NUMBER,
  ATIVO CHAR(1) NOT NULL,
  MEDIA NUMBER,
  PRIMARY KEY (ID_CANDIDATO),
  CONSTRAINT FK_CAND_ID_ED
    FOREIGN KEY (ID_EDICAO)
      REFERENCES EDICAO(ID_EDICAO)
);

CREATE TABLE LINGUAGEM_CANDIDATO (
  ID_LINGUAGEM NUMBER ,
  ID_CANDIDATO NUMBER NOT NULL,
  CONSTRAINT FK_LING_CAND_ID_LING
    FOREIGN KEY (ID_LINGUAGEM)
      REFERENCES LINGUAGEM(ID_LINGUAGEM),
  CONSTRAINT FK_LING_CAND_ID_CAND
    FOREIGN KEY (ID_CANDIDATO)
      REFERENCES CANDIDATO(ID_CANDIDATO)
);

CREATE TABLE INSCRICAO (
  ID_INSCRICAO NUMBER ,
  ID_CANDIDATO NUMBER NOT NULL,
  DATA_INSCRICAO DATE NOT NULL,
  AVALIADO CHAR(1) NOT NULL,
  PRIMARY KEY (ID_INSCRICAO)
);

CREATE TABLE AVALIACAO (
  ID_AVALIACAO NUMBER ,
  ID_INSCRICAO NUMBER NOT NULL,
  ID_GESTOR NUMBER NOT NULL,
  APROVADO CHAR(1) NOT NULL,
  PRIMARY KEY (ID_AVALIACAO),
  CONSTRAINT FK_AVA_ID_GEST
    FOREIGN KEY (ID_GESTOR)
      REFERENCES GESTOR(ID_GESTOR)
);

CREATE TABLE ENTREVISTAS (
  ID_ENTREVISTA NUMBER,
  ID_GESTOR NUMBER NOT NULL,
  ID_CANDIDATO NUMBER NOT NULL,
  LEGENDA VARCHAR2(20) NOT NULL,
  DATA_HORA TIMESTAMP NOT NULL,
  OBSERVACOES VARCHAR2(255),
  PARECECER_COMP VARCHAR2(255),
  PARECER_TECNICO VARCHAR2(255),
  PRIMARY KEY (ID_ENTREVISTA),
  CONSTRAINT FK_ENT_ID_GEST
    FOREIGN KEY (ID_GESTOR)
      REFERENCES GESTOR(ID_GESTOR),
  CONSTRAINT FK_ENT_ID_CAND
    FOREIGN KEY (ID_CANDIDATO)
      REFERENCES CANDIDATO(ID_CANDIDATO)
);

CREATE TABLE IMAGEM (
  ID_IMAGEM NUMBER ,
  ID_GESTOR NUMBER ,
  ID_CANDIDATO NUMBER ,
  NOME VARCHAR2(255) NOT NULL,
  TIPO VARCHAR2(255) NOT NULL,
  PRIMARY KEY (ID_IMAGEM),
  CONSTRAINT FK_GEST_IMG FOREIGN KEY (ID_GESTOR) REFERENCES GESTOR (ID_GESTOR),
    CONSTRAINT FK_CAND_IMG FOREIGN KEY (ID_CANDIDATO) REFERENCES CANDIDATO (ID_CANDIDATO)
);


