CREATE TABLE CARGO (
  ID_CARGO NUMBER ,
  NOME VARCHAR2(255) NOT NULL,
  PRIMARY KEY (ID_CARGO)
);

CREATE TABLE GESTOR (
  ID_GESTOR NUMBER,
  NOME VARCHAR2(255) NOT NULL,
  EMAIL VARCHAR2(255) NOT NULL,
  SENHA VARCHAR2(255) NOT NULL,
  ATIVO CHAR(1) NOT NULL,
  GENERO CHAR(1) NOT NULL,
  CIDADE VARCHAR2(255) NOT NULL,
